sub sz {
	my ($bits, $ix) = @_;
	my ($low, $width) = split(/\+/, $bits);
	my $scale = 8 * (2 ** (-$ix / 6.0));
	my $nwidth = 0;
	for ($pos = 0; $pos <= length $width; ++$pos) {
		my $c = substr $width, $pos, 1;
		$nwidth += $c * $scale;
		$scale *= 0.5;
	}
	sprintf("%12.8g", $nwidth)
}
